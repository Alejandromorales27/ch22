package com.generation.math;

public class Math {
	
	public int suma(int a, int b) {
		return a+b;
	}
	
	public double suma(double a, double b) {
		//return ((a+b)*100)/100;
		return a+b;
	}
	
	public boolean isPositive (double value) {
		return value >=0;
	}
	
	public int resta(int a, int b) {
		return a-b;
	}
	
	public double resta(double a, double b) {
		return a -b ;
	}

}
